const mongoose = require("mongoose");

const userSchema = mongoose.Schema({
  Username: {
    type: String,
    unique: true,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
    unique: true,
  },
});

module.exports = ("User", userSchema);
